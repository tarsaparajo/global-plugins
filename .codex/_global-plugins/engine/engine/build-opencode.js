'use strict';

// Build the OpenCode compiled plugin payload. The OpenCode adapter projects
// canonical components plus a TypeScript plugin/tool surface; this step compiles
// that surface into the plugin's PRIVATE bundle (.opencode/_<slug>/dist/) and
// drops a per-slug discovery loader at the SHARED .opencode/plugins/<slug>.js.
// Invoked by the canonical-projector (and a child's project.mjs) BEFORE validate()
// whenever a projection targets opencode. Run with:
//   node engine/build-opencode.js <root> <slug>
// where <slug> is the plugin's name (pluginLabel). With no slug it falls back to
// the legacy flat layout (.opencode/dist/), used only by fixtures with no manifest.

const fs = require('fs');
const path = require('path');

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

// The plugin's private bundle dir name inside .opencode/ (e.g. `_global-plugins`),
// or '' for the legacy flat layout. Kept in lock-step with helpers.privateBundleDir.
function bundleDir(slug) {
  return slug ? `_${slug}` : '';
}

// The native OpenCode plugin source emitted into the bundle's dist/. It registers
// in-process tools (<slug>-generate/adapt/evolve/validate/migrate) the OpenCode
// agent can call directly, each delegating to the deterministic projection engine
// carried in the SIBLING `engine/` runtime payload (shipped by the opencode
// adapter into the same `_<slug>/` bundle). Tools shell out via the Bun `$` API to
// `node engine/scripts/evolve/*.mjs`, so there is no bundler/toolchain dependency
// and dist/ stays small. The tool NAMES are slug-prefixed so two installed plugins
// never shadow each other (OpenCode resolves same-named tools by precedence).
// Generated DETERMINISTICALLY per slug (no timestamps/randomness) so the
// projection-drift guard can byte-compare it.
function toolsSource(slug) {
  const ns = slug ? `${slug}-` : 'global-plugins-';
  return `'use strict';
// OpenCode native tools for ${slug || 'global-plugins'}. Generated by engine/build-opencode.js.
// Each tool delegates to the projection engine in the sibling engine/ runtime
// payload. This file lives at <opencode-config>/${bundleDir(slug) || '.'}/dist/tools/index.js, so the
// payload (engine, a sibling of dist/ inside the bundle) is two levels up.
// Re-derived; do not hand-edit.

const path = require('path');

// The runtime payload lives at <bundle>/engine, a sibling of <bundle>/dist.
// From this file (<bundle>/dist/tools/index.js) that is two dirs up + engine.
function payloadRoot() {
  return path.join(__dirname, '..', '..', 'engine');
}

// Build a tool definition. OpenCode's tool() + tool.schema are provided by the
// host at load time; we accept them via the factory so this file stays
// dependency-free and testable in plain Node.
function defineTools(tool) {
  const enginePath = payloadRoot();
  const run = async (ctx, scriptArgs) => {
    // ctx provides the Bun \`$\` shell. Invoke the bundled engine wrapper.
    const { $ } = ctx;
    const script = path.join(enginePath, 'scripts', 'evolve', 'project.mjs');
    return $\`node \${script} \${scriptArgs}\`;
  };
  return {
    '${ns}generate': tool({
      description: 'Generate a multi-provider plugin from a briefing using the bundled projection engine.',
      args: { briefing: tool.schema.string(), targets: tool.schema.string().optional() },
      async execute(args, ctx) {
        return run(ctx, ['--apply', args.targets || 'all']).then(() => 'generation planned via engine');
      },
    }),
    '${ns}adapt': tool({
      description: 'Adapt an existing single-provider plugin into a multi-provider plugin via the bundled engine.',
      args: { path: tool.schema.string() },
      async execute(args, ctx) { return run(ctx, ['--apply', args.path]).then(() => 'adapt planned via engine'); },
    }),
    '${ns}evolve': tool({
      description: 'Re-project a canonical change across every provider via the bundled engine.',
      args: { change: tool.schema.string().optional() },
      async execute(_args, ctx) { return run(ctx, ['--apply']).then(() => 'evolve planned via engine'); },
    }),
    '${ns}validate': tool({
      description: 'Run the projection-drift / consistency validation gate via the bundled engine.',
      args: {},
      async execute(_args, ctx) { return run(ctx, ['--dry-run']).then(() => 'validation run via engine'); },
    }),
    '${ns}migrate': tool({
      description: 'Apply the pending migration chain to an installed copy via the bundled engine.',
      args: { path: tool.schema.string().optional() },
      async execute(args, ctx) { return run(ctx, ['--apply', args.path || '.']).then(() => 'migrate run via engine'); },
    }),
  };
}

module.exports = { defineTools, payloadRoot };
`;
}

const PLUGIN_SOURCE = `'use strict';
// OpenCode plugin entry. Generated by engine/build-opencode.js. Returns a Hooks
// object whose \`tool\` map registers the native generate/adapt/evolve/validate/
// migrate tools backed by the engine/ runtime payload. The host supplies
// tool()/tool.schema via @opencode-ai/plugin; we resolve them lazily so a missing
// SDK degrades to an empty tool map rather than crashing.

const { defineTools } = require('./tools');

const GlobalPluginsPlugin = async () => {
  let api;
  try {
    api = require('@opencode-ai/plugin');
  } catch {
    return {}; // SDK not present at load — no tools registered, no crash.
  }
  if (!api || typeof api.tool !== 'function') {
    return {};
  }
  return { tool: defineTools(api.tool) };
};

module.exports = { GlobalPluginsPlugin, defineTools };
`;

// The shared-dir discovery loader at .opencode/plugins/<slug>.js. OpenCode
// auto-loads plugins from ~/.config/opencode/plugins/ (a documented load path),
// so one thin per-slug file there makes the namespaced bundle's plugin
// discoverable WITHOUT moving the compiled code out of the private bundle and
// WITHOUT editing any shared file. Per-slug filename => no collision between
// installed plugins. It just re-exports the bundle's entry.
function loaderSource(slug) {
  const bundle = bundleDir(slug) || '.';
  return `'use strict';
// Discovery loader for ${slug || 'global-plugins'}. Generated by engine/build-opencode.js.
// Re-exports the plugin entry from this plugin's private bundle so OpenCode's
// plugins/ auto-discovery finds it without the compiled code leaving the bundle.
module.exports = { GlobalPluginsPlugin: require('../${bundle}/dist/index').GlobalPluginsPlugin };
`;
}

// Build the native OpenCode plugin payload into .opencode/_<slug>/dist/ and the
// discovery loader into .opencode/plugins/<slug>.js. Deterministic: emitted files
// carry no timestamps/randomness, so re-running yields byte-identical output and
// the drift guard can verify it like any projection.
function build(projectRoot, slug) {
  const bundle = bundleDir(slug);
  const distDir = bundle
    ? path.join(projectRoot, '.opencode', bundle, 'dist')
    : path.join(projectRoot, '.opencode', 'dist');
  ensureDir(path.join(distDir, 'plugins'));
  ensureDir(path.join(distDir, 'tools'));

  fs.writeFileSync(path.join(distDir, 'tools', 'index.js'), toolsSource(slug));
  fs.writeFileSync(path.join(distDir, 'index.js'), PLUGIN_SOURCE);
  // plugins/ barrel re-exports the plugin entry so OpenCode's loader can discover
  // it under either dist/index.js or dist/plugins/. No cycle: it pulls from
  // ../index, which does not require ./plugins.
  fs.writeFileSync(path.join(distDir, 'plugins', 'index.js'),
    "'use strict';\nmodule.exports = { GlobalPluginsPlugin: require('../index').GlobalPluginsPlugin };\n");

  const artefacts = [
    path.join('.opencode', bundle || '', 'dist', 'index.js'),
    path.join('.opencode', bundle || '', 'dist', 'plugins'),
    path.join('.opencode', bundle || '', 'dist', 'tools'),
  ];

  // Discovery loader at the shared plugins/ dir, one per slug (skipped in the
  // legacy flat layout, where dist/ already sits where the old loader expected).
  if (slug) {
    const pluginsDir = path.join(projectRoot, '.opencode', 'plugins');
    ensureDir(pluginsDir);
    fs.writeFileSync(path.join(pluginsDir, `${slug}.js`), loaderSource(slug));
    artefacts.push(path.join('.opencode', 'plugins', `${slug}.js`));
  }

  return { ok: true, artefacts };
}

if (require.main === module) {
  const root = process.argv[2] || process.cwd();
  const slug = process.argv[3] || '';
  const res = build(root, slug);
  process.stdout.write(`${JSON.stringify(res)}\n`);
}

module.exports = { build };
