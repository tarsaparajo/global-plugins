'use strict';

// Build the OpenCode compiled plugin payload. The OpenCode adapter projects
// canonical components plus a TypeScript plugin/tool surface; this step compiles
// that surface into .opencode/dist/{index.js,plugins/,tools/}. Invoked by the
// canonical-projector (and a child's project.mjs) BEFORE validate() whenever a
// projection targets opencode. Run with: node engine/build-opencode.js <root>

const fs = require('fs');
const path = require('path');

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

// The native OpenCode plugin source emitted into .opencode/dist/. It registers
// in-process tools (generate/adapt/evolve/validate/migrate) the OpenCode agent can
// call directly, each delegating to the deterministic projection engine carried in
// the sibling `_engine/` runtime payload (shipped by the opencode adapter). Tools
// shell out via the Bun `$` API to `node _engine/scripts/evolve/*.mjs`, so there is
// no bundler/toolchain dependency and `dist/` stays small. Generated DETERMINISTICALLY
// (no timestamps/randomness) so the projection-drift guard can byte-compare it.
const TOOLS_SOURCE = `'use strict';
// OpenCode native tools for global-plugins. Generated by engine/build-opencode.js.
// Each tool delegates to the projection engine in the sibling _engine/ runtime
// payload (../_engine relative to this dist/). Re-derived; do not hand-edit.

const path = require('path');

// The runtime payload lives at <opencode-config>/_engine, a sibling of dist/.
function payloadRoot() {
  return path.join(__dirname, '..', '_engine');
}

// Build a tool definition. OpenCode's tool() + tool.schema are provided by the
// host at load time; we accept them via the factory so this file stays
// dependency-free and testable in plain Node.
function defineTools(tool) {
  const enginePath = payloadRoot();
  const run = async (ctx, scriptArgs) => {
    // ctx provides the Bun \`$\` shell. Invoke the bundled engine wrapper.
    const { $ } = ctx;
    const script = path.join(enginePath, 'scripts', 'evolve', 'project.mjs');
    return $\`node \${script} \${scriptArgs}\`;
  };
  return {
    'global-plugins-generate': tool({
      description: 'Generate a multi-provider plugin from a briefing using the bundled projection engine.',
      args: { briefing: tool.schema.string(), targets: tool.schema.string().optional() },
      async execute(args, ctx) {
        return run(ctx, ['--apply', args.targets || 'all']).then(() => 'generation planned via _engine');
      },
    }),
    'global-plugins-adapt': tool({
      description: 'Adapt an existing single-provider plugin into a multi-provider plugin via the bundled engine.',
      args: { path: tool.schema.string() },
      async execute(args, ctx) { return run(ctx, ['--apply', args.path]).then(() => 'adapt planned via _engine'); },
    }),
    'global-plugins-evolve': tool({
      description: 'Re-project a canonical change across every provider via the bundled engine.',
      args: { change: tool.schema.string().optional() },
      async execute(_args, ctx) { return run(ctx, ['--apply']).then(() => 'evolve planned via _engine'); },
    }),
    'global-plugins-validate': tool({
      description: 'Run the projection-drift / consistency validation gate via the bundled engine.',
      args: {},
      async execute(_args, ctx) { return run(ctx, ['--dry-run']).then(() => 'validation run via _engine'); },
    }),
    'global-plugins-migrate': tool({
      description: 'Apply the pending migration chain to an installed copy via the bundled engine.',
      args: { path: tool.schema.string().optional() },
      async execute(args, ctx) { return run(ctx, ['--apply', args.path || '.']).then(() => 'migrate run via _engine'); },
    }),
  };
}

module.exports = { defineTools, payloadRoot };
`;

const PLUGIN_SOURCE = `'use strict';
// OpenCode plugin entry for global-plugins. Generated by engine/build-opencode.js.
// Returns a Hooks object whose \`tool\` map registers the native generate/adapt/
// evolve/validate/migrate tools backed by the _engine/ runtime payload. The
// host supplies tool()/tool.schema via @opencode-ai/plugin; we resolve them
// lazily so a missing SDK degrades to an empty tool map rather than crashing.

const { defineTools } = require('./tools');

const GlobalPluginsPlugin = async () => {
  let api;
  try {
    api = require('@opencode-ai/plugin');
  } catch {
    return {}; // SDK not present at load — no tools registered, no crash.
  }
  if (!api || typeof api.tool !== 'function') {
    return {};
  }
  return { tool: defineTools(api.tool) };
};

module.exports = { GlobalPluginsPlugin, defineTools };
`;

// Build the native OpenCode plugin payload into .opencode/dist/. Deterministic:
// the emitted files contain no timestamps or randomness, so re-running yields
// byte-identical output and the drift guard can verify it like any projection.
function build(projectRoot) {
  const distDir = path.join(projectRoot, '.opencode', 'dist');
  ensureDir(path.join(distDir, 'plugins'));
  ensureDir(path.join(distDir, 'tools'));

  fs.writeFileSync(path.join(distDir, 'tools', 'index.js'), TOOLS_SOURCE);
  fs.writeFileSync(path.join(distDir, 'index.js'), PLUGIN_SOURCE);
  // plugins/ barrel re-exports the plugin entry so OpenCode's loader can discover
  // it under either dist/index.js or dist/plugins/. Required by validate() (the
  // dir must exist + be requireable). No cycle: it pulls from ../index, which
  // does not require ./plugins.
  fs.writeFileSync(path.join(distDir, 'plugins', 'index.js'),
    "'use strict';\nmodule.exports = { GlobalPluginsPlugin: require('../index').GlobalPluginsPlugin };\n");

  return {
    ok: true,
    artefacts: [
      path.join('.opencode', 'dist', 'index.js'),
      path.join('.opencode', 'dist', 'plugins'),
      path.join('.opencode', 'dist', 'tools'),
    ],
  };
}

if (require.main === module) {
  const root = process.argv[2] || process.cwd();
  const res = build(root);
  process.stdout.write(`${JSON.stringify(res)}\n`);
}

module.exports = { build };
