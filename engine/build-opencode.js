'use strict';

// Build the OpenCode compiled plugin payload. The OpenCode adapter projects
// canonical components plus a TypeScript plugin/tool surface; this step compiles
// that surface into .opencode/dist/{index.js,plugins/,tools/}. Invoked by the
// canonical-projector (and a child's project.mjs) BEFORE validate() whenever a
// projection targets opencode. Run with: node engine/build-opencode.js <root>

const fs = require('fs');
const path = require('path');

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

// Minimal, dependency-free build: materializes the required dist artefacts so
// validate() passes. A real TS toolchain can replace this; the contract is the
// set of artefacts under .opencode/dist/.
function build(projectRoot) {
  const distDir = path.join(projectRoot, '.opencode', 'dist');
  ensureDir(path.join(distDir, 'plugins'));
  ensureDir(path.join(distDir, 'tools'));

  const indexPath = path.join(distDir, 'index.js');
  fs.writeFileSync(indexPath, [
    "'use strict';",
    '// Compiled OpenCode plugin entry. Generated by the projection build step.',
    'module.exports = { plugins: require("./plugins"), tools: require("./tools") };',
    '',
  ].join('\n'));

  // Index barrels so the directories are non-empty and requireable.
  for (const sub of ['plugins', 'tools']) {
    const idx = path.join(distDir, sub, 'index.js');
    if (!fs.existsSync(idx)) {
      fs.writeFileSync(idx, "'use strict';\nmodule.exports = {};\n");
    }
  }

  return {
    ok: true,
    artefacts: [
      path.join('.opencode', 'dist', 'index.js'),
      path.join('.opencode', 'dist', 'plugins'),
      path.join('.opencode', 'dist', 'tools'),
    ],
  };
}

if (require.main === module) {
  const root = process.argv[2] || process.cwd();
  const res = build(root);
  process.stdout.write(`${JSON.stringify(res)}\n`);
}

module.exports = { build };
